# Самый частотный символ
s = input()                         # Исходная строка
d, key, total = {}, '', 0           # Словарь, ключ и количество

for ch in s:                        # Для каждого символа в строке
    d[ch] = s.count(ch)             # Добавляем запись в словарь
    if d[ch] > total:               # Если количество больше максимального
        key, total = ch, d[ch]      # Сохраняем новый ключ, обновляем количество

print(key)                          # Выводим ключ
